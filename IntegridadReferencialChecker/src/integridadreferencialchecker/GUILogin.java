    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        // TODO add your handling code here:
        ConexionBD miCon;
        GUIMenuPrincipal ventana;
        if(txtUsuario.getText().trim().isEmpty() || txtContra.getText().trim().isEmpty() || txtBD.getText().trim().isEmpty()
                || txtServidor.getText().trim().isEmpty() || txtPuerto.getText().trim().isEmpty()){
            lblMensaje.setText("Debe llenar todos los campos");
        }else{
            miCon = new ConexionBD(txtUsuario.getText().trim(), 
                                    txtContra.getText().trim(), 
                                    txtBD.getText().trim(),
                                    txtServidor.getText().trim(),
                                    txtPuerto.getText().trim());
            if(miCon.con != null){
                ventana = new GUIMenuPrincipal(miCon);
                this.setVisible(false);
                ventana.setVisible(true);
            }
            
        }
        
    }//GEN-LAST:event_btnConectarActionPerformed

